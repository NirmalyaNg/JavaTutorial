package JavaTutorialFiles;

public class DataTypes {
    public static void main(String[] args){

        // Integral Type
        byte b = 5;
        short s = 147;
        int a = 1000;
        long large = 147885520L;

        // Floating Point Type
        float f = 10.324f;
        double d = 47.587;

        // Char Type
        char c = 'A';

        // Boolean Type
        boolean bool = true;

        System.out.println("Data Types");



    }
}
